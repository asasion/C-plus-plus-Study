#include<iostream>
using namespace std;
//从1开始数到数字100， 如果数字个位含有7，或者数字十位含有7，.
//或者该数字是7的倍数，我们打印敲桌子，其余数字直接打印输出。
int main412() {
	for (int i = 1; i <= 100; i++) {
		if (i % 10 == 7) {
			cout << "敲桌子，个位有7:" << i << endl;
		}
		else if (i / 10 % 10 == 7) {
			cout << "敲桌子，十位有7:" << i << endl;
		}
		else if (i % 7 == 0) {
			cout << "敲桌子,是7的倍数:" << i << endl;
		}
		else {
			cout << i << endl;
		}
	}


	return 0;
}