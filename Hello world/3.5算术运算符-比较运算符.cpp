#include<iostream>
using namespace std;
int main35() {
	//比较运算符
	//  ==
	int a = 10;
	int b = 20;
	cout << (a == b) << endl;//优先运算比较运算符
	// !=
	cout << (a != b) << endl;
	// >
	cout << (a > b) << endl;
	// <
	cout << (a < b) << endl;
	// >=
	cout << (a >= b) << endl;
	// <=
	cout << (a <= b) << endl;
	return 0;
}